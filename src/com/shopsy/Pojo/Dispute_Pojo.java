package com.shopsy.Pojo;

public class Dispute_Pojo 
{
	private String postedBy, postedId, userimageImage, posterName, postTime,
			postMsg, postImg;

	public String getPostedBy() {
		return postedBy;
	}

	public void setPostedBy(String postedBy) {
		this.postedBy = postedBy;
	}

	public String getPostedId() {
		return postedId;
	}

	public void setPostedId(String postedId) {
		this.postedId = postedId;
	}

	public String getUserimageImage() {
		return userimageImage;
	}

	public void setUserimageImage(String userimageImage) {
		this.userimageImage = userimageImage;
	}

	public String getPosterName() {
		return posterName;
	}

	public void setPosterName(String posterName) {
		this.posterName = posterName;
	}

	public String getPostTime() {
		return postTime;
	}

	public void setPostTime(String postTime) {
		this.postTime = postTime;
	}

	public String getPostMsg() {
		return postMsg;
	}

	public void setPostMsg(String postMsg) {
		this.postMsg = postMsg;
	}

	public String getPostImg() {
		return postImg;
	}

	public void setPostImg(String postImg) {
		this.postImg = postImg;
	}

}
