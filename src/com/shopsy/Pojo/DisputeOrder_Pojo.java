package com.shopsy.Pojo;

public class DisputeOrder_Pojo {
	private String orderId, claimAmt, buyerId, buyerImage, buyerName;

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getClaimAmt() {
		return claimAmt;
	}

	public void setClaimAmt(String claimAmt) {
		this.claimAmt = claimAmt;
	}

	public String getBuyerId() {
		return buyerId;
	}

	public void setBuyerId(String buyerId) {
		this.buyerId = buyerId;
	}

	public String getBuyerImage() {
		return buyerImage;
	}

	public void setBuyerImage(String buyerImage) {
		this.buyerImage = buyerImage;
	}

	public String getBuyerName() {
		return buyerName;
	}

	public void setBuyerName(String buyerName) {
		this.buyerName = buyerName;
	}

}
