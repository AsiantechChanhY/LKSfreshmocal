package com.shopsy.Pojo;

public class UserProfile_Pojo {
	private String username, userFullname, userimage, userlocation, usergender,
			userjoined, followersTotal, followingTotal;

	private String fav_productImage, fav_productCount;
	private String fav_shopImage, fav_shopCount, shopname;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getUserFullname() {
		return userFullname;
	}

	public void setUserFullname(String userFullname) {
		this.userFullname = userFullname;
	}

	public String getUserimage() {
		return userimage;
	}

	public void setUserimage(String userimage) {
		this.userimage = userimage;
	}

	public String getUserlocation() {
		return userlocation;
	}

	public void setUserlocation(String userlocation) {
		this.userlocation = userlocation;
	}

	public String getUsergender() {
		return usergender;
	}

	public void setUsergender(String usergender) {
		this.usergender = usergender;
	}

	public String getUserjoined() {
		return userjoined;
	}

	public void setUserjoined(String userjoined) {
		this.userjoined = userjoined;
	}

	public String getFollowersTotal() {
		return followersTotal;
	}

	public void setFollowersTotal(String followersTotal) {
		this.followersTotal = followersTotal;
	}

	public String getFollowingTotal() {
		return followingTotal;
	}

	public void setFollowingTotal(String followingTotal) {
		this.followingTotal = followingTotal;
	}

	public String getFav_productImage() {
		return fav_productImage;
	}

	public void setFav_productImage(String fav_productImage) {
		this.fav_productImage = fav_productImage;
	}

	public String getFav_productCount() {
		return fav_productCount;
	}

	public void setFav_productCount(String fav_productCount) {
		this.fav_productCount = fav_productCount;
	}

	public String getFav_shopImage() {
		return fav_shopImage;
	}

	public void setFav_shopImage(String fav_shopImage) {
		this.fav_shopImage = fav_shopImage;
	}

	public String getFav_shopCount() {
		return fav_shopCount;
	}

	public void setFav_shopCount(String fav_shopCount) {
		this.fav_shopCount = fav_shopCount;
	}

	public String getShopname() {
		return shopname;
	}

	public void setShopname(String shopname) {
		this.shopname = shopname;
	}

}
